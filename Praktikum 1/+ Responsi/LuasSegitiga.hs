module LuasSegitiga where
    -- DEFINISI DAN SPESIFIKASI
    luasSegitiga :: Float -> Float -> Float
        -- luasSegitiga a t menerima masukan 2 buah bilangan real (float) a dan t dengan a = alas segitiga dan t = tinggi segitiga (asumsikan: a > 0, t > 0) dan menghasilkan luas segitiga berdasarkan rumus: luas = 0.5 * a * t

    -- REALISASI
    luasSegitiga a t = 0.5*a*t